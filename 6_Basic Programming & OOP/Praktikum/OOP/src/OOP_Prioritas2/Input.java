package OOP_Prioritas2;

public class Input {
    
    public void add(int n, int m){
        int hasil = n + m;
        System.out.println("Penjumlahan : " +hasil);
    }
    
    public void substract(int n, int m){
        int hasil = n - m;
        System.out.println("Pengurangan : " +hasil);
    }
    
    public void multiply(int n, int m){
        int hasil = n * m;
        System.out.println("Perkalian : " +hasil);
    }
    
    public void divide(int n, int m){
        int hasil = n / m;
        System.out.println("Pembagian : " +hasil);
    }
}

